    private void dtgUsuariosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dtgUsuariosMousePressed
        // TODO add your handling code here:
        int wControle;
        int linhaSelecionada = dtgUsuarios.getSelectedRow();//pega a linha selecionada
        String coluna1 = dtgUsuarios.getModel().getValueAt(linhaSelecionada, 0).toString(); // Primeira coluna da linha
        
        if (dtgUsuarios.isColumnSelected(3)){
            tbpUsuarios.setSelectedIndex(1);
            txtNome_Usuario.requestFocus();
            btAlterar.setVisible(true);
            btExcluir.setVisible(true);
            btNovo.setVisible(true);
            btAdicionar.setVisible(false);

            ctlUsuario = new Controla_Usuarios(Usuarios, dtgUsuarios);

            Usuarios.definirId_Usuario(Integer.parseInt(coluna1));
            wControle = ctlUsuario.buscarUsuario();
                if (wControle < 0){
                CaixaDeDialogo.obterinstancia().exibirMensagem("Erro ao buscar Usuario no BD!");
                } else {
                if (wControle == 0)
                TelaAlteracao();
                }
     }
    }//GEN-LAST:event_dtgUsuariosMousePressed

